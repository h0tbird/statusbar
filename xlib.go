package main

// #cgo LDFLAGS: -lX11
// #include <X11/Xlib.h>
import "C"

//-----------------------------------------------------------------------------
// Globals:
//-----------------------------------------------------------------------------

var dpy = C.XOpenDisplay(nil)

//-----------------------------------------------------------------------------
// setStatus:
//-----------------------------------------------------------------------------

func setStatus(s string) {
	C.XStoreName(dpy, C.XDefaultRootWindow(dpy), C.CString(s))
	C.XSync(dpy, 1)
}
